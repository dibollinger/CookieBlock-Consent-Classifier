def main():
    """ Perform predictions using the """
    logger.info("Predicting Labels...")
    argv = None
    cargs = docopt(__doc__, argv=argv)

    setupLogger(None)

    model_path = cargs["<model_path>"]
    if not os.path.exists(model_path):
        logger.error(f"Model does not exist: '{model_path}'")
        return 2

    model = xgb.Booster(model_file=model_path)
    true_label, data = get_sample_input01()
    dmat = xgb.DMatrix(csr_matrix(data))

    predicted_probabilities = model.predict(dmat, training=False)
    discrete_prediction = bayesian_decision(predicted_probabilities, get_equal_loss_weights())

    np.set_printoptions(suppress=True)
    logger.info(f"Predicted Probabilities: {predicted_probabilities}")
    logger.info(f"True Label: {true_label}")
    logger.info(f"Predicted Label: {discrete_prediction}")



if __name__ == "__main__":
    exit(main())
